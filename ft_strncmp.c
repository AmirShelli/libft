int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	i = 0;
	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while ((ptr1[i] == ptr2[i]) && (ptr1[i]) && (ptr2[i]) && (i < n))
	{
		i++;
	}
	return (ptr1[i] - ptr2[i]);
}
